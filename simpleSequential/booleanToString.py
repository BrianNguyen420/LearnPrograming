# Note: Sometime we need to convert boolean to string. It can be done using a build in function.
#
# Create a function that take one boolean variable and return the string representation of the boolean.
# Ex:
#   booleanToString(True)  # return "True"
#   booleanToString(False) # return "False"
def booleanToString(var: bool) -> str:
    return str(var)