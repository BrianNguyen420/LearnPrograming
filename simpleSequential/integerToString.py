# Note: Sometime we need to convert integer to string. It can be done using a build in function.
#
# Create a function that take one integer number. Return the string representation of the number.
# Ex:
#   integerToString(690) # return "690"
#   integerToString(-69) # return "-69"
#   integerToString(000) # return "0"
def integerToString(n: int) -> str:
    return str(n)